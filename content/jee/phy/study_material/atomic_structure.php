             <h2 id="con&ins">CONDUCTORS AND INSULATORS</h2>
             </div>
             <div class="content"><p>Some substances readily allow passage of electricity through them, others do not. Those which allow electricity to pass through them easily are called conductors. They have electric charges (electrons) that are comparatively free to move inside the material. Metals, human and animal bodies and earth are conductors. Most of the non-metals like glass, porcelain, plastic, nylon, wood offer high resistance to the passage of electricity through them. They are called insulators. Most substances fall into one of the two classes stated above.</p>
                 <span class="italic-bold"><p>When some charge is transferred to a conductor, it readily gets distributed over the entire surface of the conductor. In contrast, if some charge is put on an insulator, it stays at the same place.</p></span>
            
