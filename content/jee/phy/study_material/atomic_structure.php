         <div  class="topic-heading"><h2 >ELECTRIC CHARGE</h2></div>


             <div class="content">
                 <P>Historically the credit of discovery of the fact that amber rubbed with wool or silk cloth attracts light objects goes to Thales of Miletus, Greece, around 600 BC. The name electricity is coined from the Greek word</P>
                 <P>elektron meaning amber. Many such pairs of materials were known which on rubbing could attract light objects like straw, pith balls and bits of papers.</P>
                 <P>It was observed that if two glass rods rubbed with wool or silk cloth are brought close to each other, they repel each other . The two strands of wool or two pieces of silk cloth, with which the rods were rubbed, also repel each other. However, the glass rod and wool attracted each other. Similarly, two plastic rods rubbed with cat’s fur repelled each other  but attracted the fur. On the other hand, the plastic rod attracts the glass rod [ and repel the silk or wool with which the glass rod is rubbed. The glass rod repels the fur.</P>
                 <P>These seemingly simple facts were established from years of efforts and careful experiments and their analyses. It was concluded, after many careful studies by different scientists, that there were only two kinds of an entry which is called the electric charge. We say that the bodies like glass or plastic rods, silk, fur and pith balls are electrified. They acquire an electric charge on rubbing. There are two kinds of electrification and we find that (i) like charges repel and (ii) unlike charges attract each other. The property which differentiates the two kinds of charges is called the polarity of charge.</P>
                 <p>When a glass rod is rubbed with silk, the rod acquires one kind of charge and the silk acquires the second kind of charge. This is true for any pair of objects that are rubbed to be electrified. Now if the electrified glass rod is brought in contact with silk, with which it was rubbed, they no longer attract each other. They also do not attract or repel other light objects as they did on being electrified.</p>
